    private void btn_pembayaranActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_pembayaranActionPerformed
        // TODO add your handling code here:
        int cek = 0;
        if (getId().isEmpty() || getServer().isEmpty()){
            txt_warn.setText("Masukkan id Server Terlebih dahulu");
        } else {
            cek = cek + 1;
        }
        
        if (varian().isEmpty()){
            txt_warn.setText("Pilih Varian Diamond");
        } else {
            cek = cek + 1;
        }
        
        if (cek == 2){
            JOptionPane.showMessageDialog(null, getId() + " " + getServer() + " " + varian());
            new PembayaranPage(user, pass, 1).setVisible(true);
            inputSQL();
            this.dispose();
            
        }
        
        
        
    }//GEN-LAST:event_btn_pembayaranActionPerformed

