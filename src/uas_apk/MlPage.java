        String dm = "";
        String harga = "";
        String  pilih = "";
    public String varian(){
            if (chck_86.isSelected()){
                dm = "Varian 86";
                harga = " dengan harga Rp. 20.000";
            } else if (chck_172.isSelected()){
                dm = "Varian 172";
                harga = " dengan harga Rp. 40.000";
            } else if (chck_257.isSelected()){
                dm = "Varian 257";
                harga = " dengan harga Rp. 60.000";
            } else if (chck_343.isSelected()){
                dm = "Varian 343";
                harga = " dengan harga Rp. 80.000";
            } else if (chck_429.isSelected()){
                dm = "Varian 429";
                harga = " dengan harga Rp. 100.000";
            } else {
                JOptionPane.showMessageDialog(null, "Pilih varian Diamond");
                

            }
        pilih = dm + harga;
        return pilih;
    }
    
    public void inputSQL(){
        try{
            
            String sql = "INSERT INTO Ml (username, user_Id, server_Id, varian, harga) VALUES (?,?,?,?,?);";
            pst = con.prepareStatement(sql);
            pst.setString(1, user);
            pst.setString(2, getId());
            pst.setString(3, getServer());
            pst.setString(4, dm);
            pst.setString(5, harga);
            if (pst.executeUpdate()>0){
                JOptionPane.showMessageDialog(null, "Input Berhasil");
                this.dispose();
            }
            
            
            
        }catch(Exception e){
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, "Input Failed" + e);
        } finally {
            try{
                rs.close();
                pst.close();
            }catch (Exception e){
                e.printStackTrace();
            }
            
        }
    }
    
